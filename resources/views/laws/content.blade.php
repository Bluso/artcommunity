                                    มาตรา 29 ได้กำหนดอายุของผู้ซื้อเครื่องดื่มแอลกอฮอล์ที่ 20 ปีบริบูรณ์<br>
                                    • อายุ20ปีบริบูรณ์ตามประมวลกฎหมายแพ่งถือเป็นบุคคลบรรลุนิติภาวะที่มีความสามารถกระทำนิติกรรมใดๆ ได้<br><br>

                                    • เป็นการสอดคล้องกับพระราชบัญญัติสถานบริการ พ.ศ. 2509 ตามมาตรา 16/1 ที่ห้ามผู้ได้รับอนุญาตตั้ง
                                    สถานบริการยินยอมให้ผู้มีอายุต่ำกว่า 20 ปีบริบูรณ์ เข้าไป ในสถานบริการในระหว่างทำการนั้น<br><br>

                                    • สามารถควบคุมเยาวชนที่ยังอยู่ในวัยกำลังศึกษาได้ครอบคลุมยิ่งขึ้น เพราะ โดยทั่วไปเด็กๆจะจบการศึกษาระดับ
                                    อุดมศึกษาในช่วงอายุ 21-24ปี ข้อมูลองค์การอนามัยโลกพบว่ามีอย่างน้อย 76 ประเทศที่มีกฎหมายกำหนดอายุ
                                    ขั้นต่ำในการซื้อหรือดื่มเครื่องดื่มที่มีส่วนผสมของแอลกอฮอล์ที่กำหนดอายุ ที่21ปีเช่น ชิลี เบราลูส อียิปต์ ยูเครน
                                    และบางรัฐของสหรัฐอเมริกาและที่กำหนดอายุ 20 ปี เช่น ไอซ์แลนด์ญี่ปุ่นนิวซีแลนด์ เป็นต้น<br><br>

                                    • ตามพระราชบัญญัติคุ้มครองเด็ก พ.ศ. 2546 มาตรา26(10) มีการห้ามมิให้ผู้ใดจำหน่ายแลกเปลี่ยนหรือให้สุรา
                                    แก่เด็กไม่ว่าเด็กจะยินยอมหรือไม่ก็ตาม เว้นแต่เป็นการปฏิบัติทางการแพทย์ หากฝ่าฝืนต้องระวางโทษจำคุกไม่เกิน
                                    3 เดือน หรือปรับ ไม่เกิน 30,000 บาท หรือทั้งจำทั้งปรับ และถ้ามีกฎหมายอื่นที่มีโทษหนักกว่าก็ให้ลงโทษตาม
                                    กฎหมายนั้น รวมทั้งยังมีมาตรา 45 ที่ห้ามมิให้เด็กซื้อหรือเสพสุรา หรือ เข้าไปในสถานที่เฉพาะเพื่อการจำหน่าย
                                    หรือเสพสุราหากฝ่าฝืนจะถูกเรียกผู้ปกครองมาตักเตือน ทำทัณฑ์บนต้องทำงานบริการสังคมและอาจมีการวางข้อ
                                    กำหนดอื่นใดเพื่อป้องกันมิให้เด็กกระทำความผิดขึ้นอีก หากผู้ปกครองยังละเลยปล่อยให้เด็กกระทำความผิดอีกจะ
                                    ถูกทำทัณฑ์บนและวางประกันเป็นเงินตามสมควรแก่ฐานะของผู้ปกครอง หากทำผิดทัณฑ์บน ใน2ปีจะถูกริบเงินเข้า
                                    กองทุนคุ้มครองเด็ก (มาตรา26,39และ44) (เด็ก หมายความว่า บุคคลซึ่งมีอายุต่ำกว่า 18 ปีบริบูรณ์ แต่ไม่รวมถึงผู้ที่บรรลุ
                                    นิติภาวะด้วยการสมรส) ซึ่งพระราชบัญญัติฉบับนี้ยังมีผลใช้บังคับอยู่ ณ ปัจจุบัน
                                </div>
